    public void populateMyQTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblQandAPlanter.getModel();
        dtm.setRowCount(0);
        for(Request wk : account.getWorkQueue().getWorkRequestList()){
       if (wk instanceof QA){     
        Object[] row1 = new Object[5];
        row1[0] = wk;
        row1[1] = wk.getSender();
        row1[2] = wk.getReceiver();
        row1[3] = wk.getStatus();
        row1[4] = wk.getAnswer();
        
        dtm.addRow(row1);
                }  }
    }
