    private void btnAskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAskActionPerformed
        // TODO add your handling code here:
        if(txtAreaQuestion.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(null,"Queston field is mandatory");
       }
       else{
        
        String question = txtAreaQuestion.getText();
        QA req = new QA();
        req.setMessage(question);
        req.setSender(account);
        

        //Organization allFarmerOrganizations = null;
        boolean found = false;
        for (network n : business.getNetworkList()) {
            for (subnetwork sub : n.getSubNet()) {
                if (sub.toString().equalsIgnoreCase(account.getCity())) {
                    
                        for (Enterprise e : sub.getEnterpriseDirectory().getEnterpriseList()) {
                            for (organization org : e.getOrganizationDirectory().getOrganizationList()) {
                                if (org instanceof farmer) {
                                    req.setStatus("Posted");
                                    org.getWorkQueue().getWorkRequestList().add(req);
                                    account.getWorkQueue().getWorkRequestList().add(req);
                                    found = true;
                                    
                                    break;
                                }

                                
                            }
                        }
                    }
                    // else JOptionPane.showMessageDialog(null,"No farmer organizations found in your city !");
                }

            }
        if(found){
            JOptionPane.showMessageDialog(null,"Your question is posted to the community forum");
            populateMyQTable();
            
        }

        }
    }//GEN-LAST:event_btnAskActionPerformed

